    public boolean search(int[] nums, int target) {

        if (nums == null || nums.length == 0) {
            return false;
        }

        if (nums.length == 2) {
            return (nums[0] == target || nums[1] == target);
        }

        return  binarySearch(nums, target, 0, nums.length - 1) >= 0;
    }

    private int binarySearch(int[] nums, int target, int s, int e) {
        while (s <= e) {
            int mid = (s + e) >> 1;

            if (nums[mid] == target) {
                return mid;
            }

            if (nums[s] < nums[mid]) {
                if (nums[s] > target || target > nums[mid]) {
                    s = mid + 1;
                } else {
                    e = mid;
                }
            } else if (nums[s] > nums[mid]) {
                if (target > nums[e] || target < nums[mid]) {
                    e = mid - 1;
                } else {
                    s = mid + 1;
                }
            } else {
                s++;
            }

        }

        return -1;
    }



